package edu.byuh.cis.cs203.outwit;

/**
 * The Team enum represents the different teams in a game.
 * Each team is identified by an enum constant.
 */
public enum Team {
    /**
     * Represents a neutral team.
     */
    NEUTRAL,

    /**
     * Represents a team with light-colored pieces.
     */
    LIGHT,

    /**
     * Represents a team with dark-colored pieces.
     */
    DARK;
}
